
    
string_ = "Hello, World!"
print("Original string: ",string_)

    
print("Slicing from start to position 5:")
print(string_[:5]) 

print("\nSlicing from position 7 to the end:")
print(string_[7:])  

   
print("\nSlicing between position 7 and 12:")
print(string_[7:12])  

    
print("\nSlicing the entire string with a step of 2:")
print(string_[::2])  
    
print("\nReversing the string:")
print(string_[::-1])  


print("\nSlicing using negative indexing (last 5 characters):")
print(string_[-5:])  

    
print("Empty slice (from position 5 to 5):")
print(string_[5:5])  


